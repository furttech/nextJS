'use client'


/**
 * This is the global error boundary wrapping the entire app
 * @param param0 error object
 * @returns user notification and reset action button
 */
export default function GlobalError({
  error,
  reset,
}: {
  error: Error & { digest?: string }
  reset: () => void
}) {
  return (
    <html>
      <body>
        <h2>Something went wrong!</h2>
        <button onClick={() => reset()}>Try again</button>
      </body>
    </html>
  )
}